<script setup lang="ts">
import {initFlowbite} from "flowbite";

const props = defineProps<{
  url: string;
  mcqStoreId?:  string | number | undefined
}>();

const emit = defineEmits<{
  (e: 'update:imported', value: boolean): void;
}>()

const inputRef = ref<null | HTMLInputElement>(null)
const file = ref(null)
const isLoading = ref<boolean>(false)
const closeBtn = ref<null | HTMLElement>(null)

const submitHandler = async (event: Event) => {
  file.value = event?.target?.files[0]
}

const onSubmit = async  () => {
  const formData = new FormData()
  formData.append('file', file.value)
  if (props.mcqStoreId) {
    formData.append('mcq_store_id', props.mcqStoreId.toString())
  }

  isLoading.value = true
  const { data, pending, error, refresh } = await postData(props.url, formData)
  if (error && error.value) {
    isLoading.value = false
    showToast('error', 'Failed to import data. Please try again.')
  } else {
    isLoading.value = false
    emit('update:imported', true)
    closeBtn.value?.click()
  }

}
</script>

